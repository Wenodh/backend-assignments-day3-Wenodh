const mongoose = require('mongoose');
const User = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        enum: ['user', 'admin'],
        default: 'user',
    },
});
User.plugin(require('mongoose-paginate-v2'));
module.exports = mongoose.model('User', User);
